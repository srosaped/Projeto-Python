# O Nome de uma variável não pode começar por um número
# O Nome de uma variável só pode conter os seguintes caracteres: a-z A-Z 0-9 e o _
a1=1
_a1=1
# As variáveis são Case Sensitive: é difente dizer Idade=1, idade=1 ou IDADE=1
# As variáveis não precisam de ser declaradas, isto é, não é preciso indicar o tipo de dados que a variávelk irá guardar
